	/**
	 * 添加将ApplicationListener实现为侦听器的bean。
	 * 不会影响其他侦听器，可以在不使用bean的情况下添加其他侦听器。
	 */
	protected void registerListeners() {
		// 首先注册静态指定的侦听器。
		for (ApplicationListener<?> listener : getApplicationListeners()) {
			getApplicationEventMulticaster().addApplicationListener(listener);
		}

		// 不要在这里初始化FactoryBean:
		// 		我们需要保持所有常规bean未初始化，以便让后处理程序应用它们!
		String[] listenerBeanNames = getBeanNamesForType(ApplicationListener.class, true, false);
		for (String listenerBeanName : listenerBeanNames) {
			getApplicationEventMulticaster().addApplicationListenerBean(listenerBeanName);
		}

		// 发布早期的应用事件，现在我们终于有了一个多播器…
		Set<ApplicationEvent> earlyEventsToProcess = this.earlyApplicationEvents;
		this.earlyApplicationEvents = null;
		if (earlyEventsToProcess != null) {
			for (ApplicationEvent earlyEvent : earlyEventsToProcess) {
				getApplicationEventMulticaster().multicastEvent(earlyEvent);
			}
		}
	}

	/**
	 * 完成这个上下文bean工厂的初始化，初始化所有剩余的单例bean。
	 */
	protected void finishBeanFactoryInitialization(ConfigurableListableBeanFactory beanFactory) {
		// 为此上下文初始化转换服务。
		if (beanFactory.containsBean(CONVERSION_SERVICE_BEAN_NAME) &&
				beanFactory.isTypeMatch(CONVERSION_SERVICE_BEAN_NAME, ConversionService.class)) {
			beanFactory.setConversionService(
					beanFactory.getBean(CONVERSION_SERVICE_BEAN_NAME, ConversionService.class));
		}

		// 如果以前没有bean后处理器(比如PropertyPlaceholderConfigurer bean)注册过任何bean
		// ，那么注册一个默认的内嵌值解析器:此时，主要是为了解析注释属性值
		if (!beanFactory.hasEmbeddedValueResolver()) {
			beanFactory.addEmbeddedValueResolver(new StringValueResolver() {
				@Override
				public String resolveStringValue(String strVal) {
					return getEnvironment().resolvePlaceholders(strVal);
				}
			});
		}

		// 尽早初始化LoadTimeWeaverAware bean，以便尽早注册它们的转换器
		String[] weaverAwareNames = beanFactory.getBeanNamesForType(LoadTimeWeaverAware.class, false, false);
		for (String weaverAwareName : weaverAwareNames) {
			getBean(weaverAwareName);
		}

		// 停止使用临时类加载器进行类型匹配。
		beanFactory.setTempClassLoader(null);

		// 允许缓存所有bean定义元数据，而不期望进一步的更改。
		beanFactory.freezeConfiguration();

		// 实例化所有剩余的(非延迟-init)单例。
		beanFactory.preInstantiateSingletons();
	}

	/**
	 * 调用LifecycleProcessor的onRefresh()方法并发布{@link ContextRefreshedEvent}，完成上下文的刷新。
	 */
	protected void finishRefresh() {
		// 为这个上下文初始化生命周期处理器。
		initLifecycleProcessor();

		// 首先将刷新传播到生命周期处理器。
		getLifecycleProcessor().onRefresh();

		// 发布最终事件。
		publishEvent(new ContextRefreshedEvent(this));

		// 参与LiveBeansView MBean，如果是活动的。
		LiveBeansView.registerApplicationContext(this);
	}

