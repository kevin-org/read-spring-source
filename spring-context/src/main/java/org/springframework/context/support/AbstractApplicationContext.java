	/**
	 * 断言此上下文的BeanFactory当前是活动的，如果不是，抛出{@link IllegalStateException}。
	 * <p>被依赖于活动上下文的所有{@link BeanFactory}委托方法调用，特别是所有bean访问器方法。
	 * <p>默认实现检查{@link #isActive() 此上下文的“活动”}状态。
	 * 如果{@link #getBeanFactory()}本身在这种情况下抛出异常，则可能会被覆盖用于更具体的检查，或者用于无操作。
	 */
	protected void assertBeanFactoryActive() {
		if (!this.active.get()) {
			if (this.closed.get()) {
				throw new IllegalStateException(getDisplayName() + " has been closed already");
			}
			else {
				throw new IllegalStateException(getDisplayName() + " has not been refreshed yet");
			}
		}
	}


